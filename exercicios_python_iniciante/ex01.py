"""
Crie um programa Python para ler dois números e encontrar a soma deles.
"""

# Solicite ao usuário que insira dois números
a = float(input('Informe o primeiro valor: '))
b = float(input('Informe o segundo valor: '))

# Calcule a soma dos dois números
soma = a + b

# Exiba o resultado
print(f'A soma de {a} e {b} é igual a {soma}')